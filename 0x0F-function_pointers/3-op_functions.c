#include "3-calc.h"

/**
 * op_add - Return the sum of two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - Returns the sub
 * @a: the first number
 * @b: the second number
 *
 * Return: the diff of a and b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - returns the multiplication of two
 * @a: the first number
 * @b: the second number
 *
 * Return: the multiplication of two num
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the div ot two num
 * @a: the 1 num
 * @b: the 2 num
 *
 * Return: the quotient od a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the mod
 * @a: 1 num
 * @b: 2 num
 *
 * Return: the remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}


